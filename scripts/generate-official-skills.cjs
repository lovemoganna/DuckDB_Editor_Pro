/**
 * Generate Official Handbook Skills
 *
 * Reads skill markdown files from .agent/skills/duckdb-ai-schema/
 * and generates TypeScript types in services/skills/definitions/official-skills.ts
 *
 * Usage: node scripts/generate-official-skills.cjs
 */

const fs = require('fs');
const path = require('path');

// Resolve paths relative to this script file
const SCRIPT_DIR = path.resolve(__dirname);
const PROJECT_ROOT = path.resolve(SCRIPT_DIR, '..');
const SKILLS_SOURCE_DIR = path.resolve(PROJECT_ROOT, '.agent/skills/duckdb-ai-schema');
const OUTPUT_FILE = path.resolve(PROJECT_ROOT, 'services/skills/definitions/official-skills.ts');

const files = fs.readdirSync(SKILLS_SOURCE_DIR).filter(f => f.endsWith('.md'));

const officialSkills = files.map(file => {
  const content = fs.readFileSync(path.join(SKILLS_SOURCE_DIR, file), 'utf8');
  const titleMatch = content.match(/^# (SKL-[^:\s]+):?\s*(.*)/m);
  const id = titleMatch ? titleMatch[1] : file.replace('.md', '').toUpperCase();
  const name = titleMatch ? titleMatch[2].trim() : file;

  return {
    id,
    name,
    content,
    fileName: file
  };
});

const generatedAt = new Date().toISOString().split('T')[0];

const tsContent = `/**
 * DuckDB Official Handbook Skills - Generated from .agent/skills
 *
 * AUTO-GENERATED by scripts/generate-official-skills.cjs
 * Source: ${SKILLS_SOURCE_DIR}
 * Generated: ${generatedAt}
 * Total: ${officialSkills.length} official rules/standards.
 *
 * DO NOT EDIT MANUALLY — re-run the generator script to update.
 */

export interface OfficialHandbookSkill {
  id: string;
  name: string;
  content: string;
  fileName: string;
}

export const OFFICIAL_HANDBOOK_SKILLS: OfficialHandbookSkill[] = ${JSON.stringify(officialSkills, null, 2)};
`;

fs.writeFileSync(OUTPUT_FILE, tsContent);
console.log(`Successfully generated ${OUTPUT_FILE} with ${officialSkills.length} skills.`);
console.log(`Source: ${SKILLS_SOURCE_DIR}`);
console.log(`Run: node scripts/generate-official-skills.cjs to regenerate.`);
